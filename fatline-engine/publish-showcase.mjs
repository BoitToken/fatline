#!/usr/bin/env node
// Copy a run's passing builds into a GitHub-Pages showcase dir + a landing page.
// Prints a JSON list of {brand,type,score,url} for the Telegram message.
import { readFileSync, copyFileSync, mkdirSync, writeFileSync, rmSync, existsSync } from 'node:fs';
import path from 'node:path';

const arg = (n, d) => { const i = process.argv.indexOf(`--${n}`); return i !== -1 ? process.argv[i + 1] : d; };
const runDir = arg('run');
const out = arg('out', '/tmp/fl-showcase');
const base = arg('base', 'https://boittoken.github.io/fatline/');

const report = JSON.parse(readFileSync(path.join(runDir, 'report.json'), 'utf8'));
const winners = report.results.filter((r) => r.pass);

rmSync(out, { recursive: true, force: true });
mkdirSync(out, { recursive: true });

const slug = (s) => (s || 'build').toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '').slice(0, 40);
const items = [];
const seen = new Set();
for (const w of winners) {
  let s = slug(w.brand || w.label);
  while (seen.has(s)) s += '-x';
  seen.add(s);
  const src = path.join(runDir, w.label, 'index.html');
  if (!existsSync(src)) continue;
  copyFileSync(src, path.join(out, `${s}.html`));
  items.push({ brand: w.brand || w.label, type: w.label.replace(/^b\d+-/, ''), score: w.score, img: w.img, url: base + s + '.html' });
}

const cards = items.map((it) => `
  <a class="card" href="${it.url}" target="_blank">
    <div class="t">${it.brand}</div>
    <div class="m"><span>${it.type}</span><span class="s">${it.score}/10</span></div>
  </a>`).join('');

writeFileSync(path.join(out, 'index.html'), `<!doctype html><html lang=en><head><meta charset=utf-8>
<meta name=viewport content="width=device-width,initial-scale=1"><title>Fatline — FatBots Showcase</title>
<link href="https://fonts.googleapis.com/css2?family=Inter:wght@400;600;700&family=Space+Grotesk:wght@600;700&display=swap" rel=stylesheet>
<style>
*{box-sizing:border-box}body{margin:0;background:#07070d;color:#ecebfb;font-family:Inter,sans-serif;padding:40px 20px}
.wrap{max-width:1000px;margin:0 auto}h1{font-family:'Space Grotesk';font-size:34px;margin:0 0 6px}
.sub{color:#8a88ab;margin-bottom:28px}
.grid{display:grid;grid-template-columns:repeat(auto-fill,minmax(240px,1fr));gap:16px}
.card{display:block;background:#11101c;border:1px solid rgba(124,110,252,.18);border-radius:16px;padding:20px;text-decoration:none;color:inherit;transition:.2s}
.card:hover{transform:translateY(-3px);border-color:rgba(124,110,252,.5)}
.t{font-family:'Space Grotesk';font-weight:700;font-size:18px;margin-bottom:8px}
.m{display:flex;justify-content:space-between;color:#8a88ab;font-size:13px}.s{color:#38e0c8;font-weight:700}
</style></head><body><div class=wrap>
<h1>Fatline · FatBots Showcase</h1>
<div class=sub>${items.length} prototypes generated by the Fatline engine — each beats the V2 quality bar (audited).</div>
<div class=grid>${cards}</div></div></body></html>`);

console.log(JSON.stringify({ count: items.length, items }, null, 0));
